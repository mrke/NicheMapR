             SHADE=REFSHD
             CALL SELDEP(TSOIL,HSOIL,ZSOIL,RELHUM,PSOIL,TCOND)
            ENDIF
            DEPSEL(IHOUR)=NEWDEP*(-1.0)
           ELSE
C           NO BURROW ALLOWED
            CALL ABOVEGROUND
           ENDIF
           GOTO 253
          ENDIF
         ELSE
          CALL ABOVEGROUND
         ENDIF
         GOTO 253
        ENDIF
