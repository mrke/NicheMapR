        !PONDMIN=WETLANDTEMPS(HOURCOUNT-IHOUR+1)
       ELSE
        POND_DEPTH=POND_ENV(IYEAR,COUNTDAY,IHOUR,2)
        TWATER=POND_ENV(IYEAR,COUNTDAY,IHOUR,1)
        PONDMAX=POND_ENV(IYEAR,COUNTDAY,1,1)
        PONDMIN=POND_ENV(IYEAR,COUNTDAY,1,1)
       ENDIF
