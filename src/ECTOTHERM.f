       CALL DOPRI5(N,ONELUMP,X,YY,XEND,RTOL,ATOL,ITOL,SOLOUT,IOUT,
     &   WORK,LWORK,IWORK,LIWORK,RPAR,IPAR,IDID,VSTI)
       TC=YY(2)
