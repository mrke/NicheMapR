C         ENDIF
          IF((DEAD.EQ.1).OR.(DAYCOUNT.LT.STARTDAY))THEN
            X = TA
            TC = X
            Y(NEQ)=X
            CALL BELOWGROUND
            CALL RADIN           
            DEPSEL(IHOUR)=-1*ZSOIL(SOILNODE)
           GOTO 252          
