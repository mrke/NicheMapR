         IF(GUTFULL.LT.GUTFILL)THEN
          DEPSEL(IHOUR)=0
          INWATER=1
          FORAGE=1
          IF(AQUABASK.LT.2)THEN
           FLTYPE=1
           TC=TWATER
          ELSE
           FLTYPE=0
          ENDIF
          IF ((DAYACT .EQ. 'Y') .OR. (DAYACT .EQ. 'Y')) THEN
