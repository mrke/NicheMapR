        ELSE
         IF(PONDMAX.GT.TMAXPR)THEN
          TWATER=PONDMIN
         ELSE
          TWATER=PONDMAX
         ENDIF
         DO 70 I=1,20
          IF(TWATERS(I).EQ.TWATER)THEN
           POND_DEPTH = 21-I
           PO2WATER = O2WATERS(I)
           SALWATER = SALWATERS(I)
          ENDIF
70       CONTINUE
        ENDIF
71      WETLANDDEPTHS(HOURCOUNT)=POND_DEPTH
        !POND_DEPTH=WETLANDDEPTHS(HOURCOUNT)
