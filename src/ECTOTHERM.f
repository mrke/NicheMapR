       ENDIF
      ENDIF
      FLTYPE=ECTOINPUT(2)
C     CHECK IF AQUATIC STAGE AND, IF SO, MAKE ITS ENVIRONMENT WATER
      IF((DEB1.EQ.1).AND.(POND.EQ.0).AND.((AQUABREED.EQ.1).OR.
     &    (AQUABREED.EQ.2)))THEN
       IF(AQUABREED.EQ.1)THEN
        IF(STAGE.LE.1)THEN
