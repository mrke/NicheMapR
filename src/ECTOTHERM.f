       ENARY7=ATSOIL(:,1)
       ENARY8=0.0 ! FULL SUN
       
       SHADE=0.
       QSOLR=ENARY1(IHOUR)
       TA=ENARY3(IHOUR)
       TSUBST=ENARY7(IHOUR)
       TSKY=ENARY6(IHOUR)
       RELHUM=ENARY6(IHOUR)
      
C################################################################
C      SETTING UP CALL TO DOPRI INTEGRATOR

