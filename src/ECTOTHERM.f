C          NO BURROW ALLOWED
           CALL ABOVEGROUND
          ENDIF
          GOTO 253
         ENDIF
        ELSE
C        SUN ABOVE HORIZON AND DIURNAL
         IF((RAINACT.EQ.'Y').OR.(RAINACT.EQ.'Y'))THEN
          IF(RAINFALL.GT.RAINTHRESH)THEN
