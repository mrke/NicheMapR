              SHADE=REFSHD
              CALL SELDEP(TSOIL,HSOIL,ZSOIL,RELHUM,PSOIL,TCOND)
             ENDIF
             DEPSEL(IHOUR)=NEWDEP*(-1.0)
            ELSE
C            NO BURROW ALLOWED
             CALL ABOVEGROUND
            ENDIF
            GOTO 253
           ENDIF
          ELSE
           CALL ABOVEGROUND
          ENDIF
          GOTO 253
         ELSE
C         NO CREPUSCULAR
          IF((BURROW.EQ.'Y').OR.(BURROW.EQ.'Y'))THEN
           IF(BURROWSHADE.EQ.1)THEN
            SHADE=MAXSHD
            CALL SELDEP(TSHSOI,HSHSOI,ZSOIL,RELHUM,PSHSOI,SHTCOND)
           ELSE
            SHADE=REFSHD
            CALL SELDEP(TSOIL,HSOIL,ZSOIL,RELHUM,PSOIL,TCOND)
