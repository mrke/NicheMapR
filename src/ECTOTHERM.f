          TESTX=ENBERR
          REFTOL=TESTX
          X=ZBRENT(FUN,X1,X2,TESTX)
          IF(X.GT.80.)THEN
           X=TSOIL(1)
          ENDIF
C         OUT COMES THE GUESSED VARIABLE VALUE (X) THAT SATISFIES
