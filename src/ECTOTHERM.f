      IF((DEB1.EQ.1).AND.(POND.EQ.0))THEN
       IF(STAGE.LT.1)THEN
        IF((VIVIPAROUS.EQ.1).OR.((RESET.GT.0).AND.(E_H_START.EQ.0).AND.
